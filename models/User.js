const mongoose = require('mongoose');

// Create Schema
const UserSchema = mongoose.Schema({
  //_id: mongoose.Schema.Types.ObjectId,

  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true,
    lowercase: true
  },
  password: {
    type: String,
    required: true
  },
  avatar: {
    type: String
  },
  date: {
    type: Date,
    default: Date.now
  }
});

module.exports = User = mongoose.model('users', UserSchema);  // collection will be 'users'